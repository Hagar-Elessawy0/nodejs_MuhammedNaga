/* Comparison Operators
    ==  : Equal to            => check if two values are equal
    === : Strict Equal to     => check if two values are equal and of the same type
    !=  : Not Equal to        => check if two values are not equal
    !== : Strict Not Equal to => check if two values are not equal and of the same type
    >   : Greater than        
    <   : Less than           
    >=  : Greater than or equal to
    <=  : Less than or equal to    
 */

// Equal to
console.log(1 == 1);           // true
//console.log(1 == "1");         // true

// Strict Equal to
console.log(1 === 1);          // true
//console.log(1 === "1");        // false

// Not Equal to
console.log(1 != 1);           // false
//console.log(1 != "1");         // false

// Strict Not Equal to
console.log(1 !== 1);          // false
//console.log(1 !== "1");        // true

// Greater than
console.log(2 > 1);            // true

// Less than
console.log(1 < 2);            // true

// Greater than or equal to
console.log(2 >= 2);           // true

// Less than or equal to
console.log(2 <= 2);           // true
