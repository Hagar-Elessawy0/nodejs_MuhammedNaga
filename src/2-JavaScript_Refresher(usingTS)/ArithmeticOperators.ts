/* Arithmetic Operators 
    + : Addition
    - : Subtraction
    * : Multiplication
    / : Division
    % : Modulus
 */

let number1 = 21;
let number2 = 5;

// Addition
const sum = number1 + number2;
console.log("Addition : " + sum);                       // 26

// Subtraction
const sub1 = number1 - number2;
console.log("Subtraction : " + sub1);                    // 16
const sub2 = number2 - number1;
console.log("Subtraction : " + sub2);                    // -15

// Multiplication
const mul = number1 * number2;
console.log("Multiplication : " + mul);                 // 105

// Division
const div = number1 / number2;
console.log("Division : " + div);                       // 4.2

// Modulus
const mod = number1 % number2;
console.log("Modulus : " + mod);                        // 1

